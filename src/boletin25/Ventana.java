    private void bResultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bResultadoActionPerformed
        numerosPremiados.clear();
        totalAciertos=0;
   
        if(contNum==6){
        
            for(int i=0;i<6;i++){
                int aleatorio = (int) (Math.random()*48 + 1);
                numerosPremiados.add(aleatorio);
            } 
                aciertos.setText(numerosPremiados.get(0).toString());
                aciertos1.setText(numerosPremiados.get(1).toString());
                aciertos2.setText(numerosPremiados.get(2).toString());
                aciertos3.setText(numerosPremiados.get(3).toString());
                aciertos4.setText(numerosPremiados.get(4).toString());
                aciertos5.setText(numerosPremiados.get(5).toString());

                for(int i=0;i<numerosPremiados.size();i++){
                    for(int j=0;j<numeros.size();j++){
                        if(numerosPremiados.get(i)==numeros.get(j)){
                            totalAciertos++;
                        }
                    }
                }
                numAciertos.setText(Integer.toString(totalAciertos));
        }else{
            JOptionPane.showMessageDialog(null, "No has introducido 6 nÃºmeros");
        }    
    }//GEN-LAST:event_bResultadoActionPerformed

